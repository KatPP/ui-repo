/**
 * Применяем substring() на практике
 * Получение подстрок — это распространённая операция. Например, в банковских приложениях запрещено показывать номер карты без согласия пользователя из-за требований к защите информации.
 * Поэтому строка  "1111 2222 3333 4444" должна быть заменена на строку "1111 **** **** 4444". Для этого нужно получить подстроку из первых  4 цифр номера карты, добавить к ней 8 недостающих символов '*', а затем ещё подстроку из 4 последних цифр.
 * Попробуйте реализовать такое сокрытие строки самостоятельно — напишите метод maskCardNumber, который принимает строку с номером карты из 16 символов и оставляет в ней только первые четыре и последние четыре символа. Остальные замените на *.
 */

public class CardNumberMasker {

    public String maskCardNumber(String cardNumber) {
        return cardNumber.substring(0, 4) + "********" + cardNumber.substring(cardNumber.length() - 4);
    }


    public static void main(String[] args) {
        CardNumberMasker masker = new CardNumberMasker();
        System.out.println(masker.maskCardNumber("1234567890123456")); // Должно вывести "1234********3456"
    }
}

